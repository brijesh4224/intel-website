function loaddata(x) {

    console.log(x);
    var data = x;
    var val = document.getElementById('b_data');
    val.innerHTML = data.map((ele, ind) => {
        return `
                    <div class="col-lg-4 col-md-6 col-12" onclick="showdata('${ele.name}')">
                        <div class="   d-flex align-items-center ${ele.link ? 'p-md-3 text-center fs-1' : 'p-md-4'} p-3 h-100" style="background-color: #F3F3F3;">
                            ${ele.icon ? `<img src="${ele.icon}" class="pe-3">` : ''}
                            <h5 class="mb-0"><a href="${ele.link ? ele.link : '#'}" class="${ele.link ? 'p-md-3 text-center fs-6' : ''} b_special_a3">${ele.name}</a>
                                </h5>
                        </div>
                    </div>  
        `;
    }).join('');
}
